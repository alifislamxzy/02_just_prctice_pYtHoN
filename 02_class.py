# Basic Class 
class Employee:
    name = "Alif"  # This is Class Attribute
    language = "py"
    salary = 1000

alif = Employee() # This is Object Attribute
print(alif.name, alif.language, alif.salary)
rohan = Employee() # This is Object attribute
rohan.name = "Rohan Roro" # This is Power of Object Attribute, The name of this attribute instanc Attribute
print(rohan.name,  rohan.language, rohan.salary)
